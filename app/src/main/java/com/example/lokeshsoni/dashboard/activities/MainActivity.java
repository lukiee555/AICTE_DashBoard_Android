    private void prepareInitiatives() {

        final String url = "http://aicte.herokuapp.com/initiative";

        showpDialog();
        RequestQueue requestQueue = Volley.newRequestQueue(this);
        StringRequest getRequest = new StringRequest(Request.Method.GET, url,
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {
                        // response
                        Log.d("Response", response);
                        List<InitiativesCardView> data = new ArrayList<>();
                        try {
                            JSONArray jsonArray = new JSONArray(response);

                            for(int i = 0; i < jsonArray.length();i++){

                                JSONObject jsonObject = jsonArray.getJSONObject(i);
                                String name = jsonObject.getString("name");
                                String desc = jsonObject.getString("desc");
                                String id = jsonObject.getString("id");
                                InitiativesCardView initiatives = new InitiativesCardView(id,name,R.drawable.aicte,desc);
                                data.add(initiatives);

                            }

                            initiativesAdapter = new InitiativesAdapter(MainActivity.this, data);
                            RecyclerView.LayoutManager layoutManager = new LinearLayoutManager(getApplicationContext());
                            recyclerView.setLayoutManager(layoutManager);
                            recyclerView.setItemAnimator(new DefaultItemAnimator());
                            recyclerView.setAdapter(initiativesAdapter);


                        } catch (JSONException e) {
                            e.printStackTrace();
                        }
                        hidepDialog();

                    }
                },
                new Response.ErrorListener() {
                    @Override
                    public void onErrorResponse(VolleyError error) {
                        // error
                        Log.d("Error.Response", error.toString());
                        hidepDialog();
                        Toast.makeText(MainActivity.this,error.toString(),Toast.LENGTH_SHORT).show();
                    }
                }
        ) {

        };
        requestQueue.add(getRequest);

    }


